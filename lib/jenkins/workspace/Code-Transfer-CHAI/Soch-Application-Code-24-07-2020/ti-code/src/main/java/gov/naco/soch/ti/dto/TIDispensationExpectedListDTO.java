public class TIDispensationExpectedListDTO {
	
	private Long benficiaryId;
	private String chooseDrug;
	private String dosage;
	private String firstName;
	private String middleName;
	private String lastName;
	private String age;
	private String gender;
	public Long getBenficiaryId() {
		return benficiaryId;
	}
	public void setBenficiaryId(Long benficiaryId) {
		this.benficiaryId = benficiaryId;
	}
	public String getChooseDrug() {
		return chooseDrug;
	}
	public void setChooseDrug(String chooseDrug) {
		this.chooseDrug = chooseDrug;
	}
	public String getDosage() {
		return dosage;
	}
	public void setDosage(String dosage) {
		this.dosage = dosage;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	
}
